/**
 * C5Ejer5.java
 * 
 * Programa que muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando
un bucle while.
 * 
 * @author ÁngelMB
 */
public class C5Ejer5 {
  public static void main(String[] args) {

    int i = 360;

    while (i >= 160){
      System.out.println(i);
      i = i - 20;
    }
  }
}
