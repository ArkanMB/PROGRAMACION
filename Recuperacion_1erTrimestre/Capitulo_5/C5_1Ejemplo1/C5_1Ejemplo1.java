package C5_1Ejemplo1;

/**
 * @file: C5_Ejemplo1.java
 * 
 * @info: Programa que muestra por pantalla los números pares entre 0 y 20.
 * 
 * @author: José Ángel Moya Baena
 */

public class C5_1Ejemplo1 {

  public static void main(String[] args) {

    // Usamos este bucle for para reiterar hasta que i sea menor o igual a 20
    for (int i = 0; i <= 20; i = i + 2) {

      System.out.println("\n"+i);

    }

  }

}